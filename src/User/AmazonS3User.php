namespace Backup\User;

use Backup\Uploader\AmazonS3Uploader;

class AmazonS3User extends User
{
    protected $bucket;
    protected $region;
    protected $profile; //AKA Credential set found in ~/.aws/credentials

    /**
     * @return mixed
     */
    public function getBucket()
    {
        return $this->bucket;
    }

    /**
     * @param mixed $bucket
     */
    public function setBucket($bucket)
    {
        $this->bucket = $bucket;
    }

    /**
     * @return mixed
     */
    public function getRegion()
    {
        return $this->region;
    }

    /**
     * @param mixed $region
     */
    public function setRegion($region)
    {
        $this->region = $region;
    }

    /**
     * @return mixed
     */
    public function getProfile()
    {
        return $this->profile;
    }

    /**
     * @param mixed $profile
     */
    public function setProfile($profile)
    {
        $this->profile = $profile;
    }

    public static function getUploaderClass()
    {
        return AmazonS3Uploader::class;
    }

}